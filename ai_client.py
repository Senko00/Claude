def _build_prompt(style, topic, facts, avoid_titles) -> str:
    style_key = style if style in STYLES else None
    if style_key is None:
        import random

        style_key = random.choice(list(STYLES.keys()))
    style_instruction = STYLES[style_key]["instruction"]

    topic = topic or "на твой выбор из жизни завода"

    facts_block = ""
    if facts:
        facts_block = (
            f"Используй только следующие факты, ничего не выдумывай сверх них:\n{facts}\n"
        )

    avoid_block = ""
    if avoid_titles:
        titles = "\n".join(f"- {t}" for t in avoid_titles)
        avoid_block = f"Не повторяй темы уже опубликованных постов:\n{titles}\n"

    return PROMPT_TEMPLATE.format(
        style_example=STYLE_EXAMPLE,
        style_instruction=style_instruction,
        topic=topic,
        facts_block=facts_block,
        avoid_block=avoid_block,
    ), style_key


def generate_news(style=None, topic=None, facts=None, avoid_titles=None) -> dict:
    if not config.YANDEX_API_KEY or not config.YANDEX_FOLDER_ID:
        raise RuntimeError("YANDEX_API_KEY / YANDEX_FOLDER_ID не заданы в .env")

    prompt, used_style = _build_prompt(style, topic, facts, avoid_titles or [])

    response = requests.post(
        YANDEXGPT_URL,
        headers={
            "Content-Type": "application/json",
            "Authorization": f"Api-Key {config.YANDEX_API_KEY}",
            "x-folder-id": config.YANDEX_FOLDER_ID,
        },
        json={
            "modelUri": f"gpt://{config.YANDEX_FOLDER_ID}/{config.YANDEX_MODEL}/latest",
            "completionOptions": {
                "stream": False,
                "temperature": 0.95,
                "maxTokens": "2000",
            },
            "messages": [{"role": "user", "text": prompt}],
        },
        timeout=60,
    )
    if not response.ok:
        raise RuntimeError(
            f"YandexGPT API вернул ошибку {response.status_code}: {response.text}"
        )
    raw_text = response.json()["result"]["alternatives"][0]["message"]["text"]

    cleaned = re.sub(r"^```(json)?|```$", "", raw_text.strip(), flags=re.MULTILINE).strip()
    news = json.loads(cleaned)
    news["style"] = used_style
    return news


def generate_image(prompt: str, out_path: Path) -> None:
    url = f"https://image.pollinations.ai/prompt/{urllib.parse.quote(prompt)}"
    response = requests.get(
        url, params={"width": 1024, "height": 576, "nologo": "true"}, timeout=120
    )
    response.raise_for_status()
    out_path.write_bytes(response.content)
